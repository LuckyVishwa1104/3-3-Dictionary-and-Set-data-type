#13. clear()- used to clear or empty entire set.
# Syntax:   set_name.clear()  
set1={2,4,6,8,10}
set1.clear()
print(set1)
set2={"a","e","i","o","u"}
set2.clear()
print(set2)
# it return the None value.
set3={"Sunday","Monday","Tuesday","Wednesday","Thursday","Friday","Saturday" }
a=set3.clear()
print(a)
set4={True,False,bool(2),bool(""),None}
set4.clear()
print(set4)
set5={"Python","Java","C","C++","sql","JavaScript"}	
set5.clear()
print(set5)
